package javaprograms;

public class doWhileLoop {
	// in dowhile we have first loop then condition,its like first do then apply condition.the loop executive even once)

	public static void main(String[] args) {
	int i=20;
	
	do 
	{
		System.out.println(i);
		i++;
	} while(i<=10);
	
	// output is 20

	}

}
